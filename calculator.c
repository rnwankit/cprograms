#include<stdio.h>

int main() {
	int x=10;
	int y=5;
	
	int add, sub, mul, div;
	
	add = x + y;
	sub = x - y;
	mul = x * y;
	div = x / y;
	
	printf("Addition is: %d\n", add);
	printf("Substration is: %d\n", sub);
	printf("Multiplication is: %d\n", mul);
	printf("Division is: %d", div);
	
	return 0;
}
